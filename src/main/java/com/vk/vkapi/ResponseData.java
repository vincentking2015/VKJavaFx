public class ResponseData<T> {
    private int code;

    private String msg;

    private T data;

    private String time;

    public ResponseData(int code, String msg, T data, String time) {
        this.code = code;
        this.msg = msg;
        this.data = data;
        this.time = time;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public T getData() {
        return data;
    }

    public void setData(T data) {
        this.data = data;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
}
